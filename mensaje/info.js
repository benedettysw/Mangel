var mensajeVisible = false;

function mostrarOcultarMensaje() {
  var mensajeElement = document.getElementById("mensaje");
  
  if (mensajeVisible) {
    mensajeElement.style.display = "none";
    mensajeVisible = false;
  } else {
    mensajeElement.textContent = "Puede descarga windows en formato (ISO) en su pagina oficial o en mediafire";
    mensajeElement.style.display = "block";
    mensajeElement.style.color = "white";
    mensajeVisible = true;
  }
}



